# Startup Tagline Generator
# Demonstrating Strings, String Operations, Concatenation, String Methods, and User Input

def main():
    print("--- Startup Tagline Generator ---")
    
    # Example of user input
    # name = input("Enter your name: ")
    name = "Sample Name"
    
    # String Operations & Concatenation
    greeting = "Hello, " + name + "!"
    
    # String Methods
    formatted_greeting = greeting.upper()
    
    print(formatted_greeting)
    
if __name__ == "__main__":
    main()
